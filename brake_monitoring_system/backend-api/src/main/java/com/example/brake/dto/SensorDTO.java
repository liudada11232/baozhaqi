package com.example.brake.dto;

public class SensorDTO {
    private double temp;
    private double vib;
    private double gap;

    // Getters and Setters
    public double getTemp() { return temp; }
    public void setTemp(double temp) { this.temp = temp; }
    public double getVib() { return vib; }
    public void setVib(double vib) { this.vib = vib; }
    public double getGap() { return gap; }
    public void setGap(double gap) { this.gap = gap; }
}
